def _build_preview(tool_name: str, args: dict) -> str:
    if tool_name == "start_compliance_workflow":
        worker_id = args.get("worker_id", "")
        doc = db.collection("workers").document(worker_id).get()
        name = worker_id
        if doc.exists:
            name = _worker_name(doc.to_dict() or {}, worker_id)
        return f"Start compliance workflow for **{name}** ({worker_id})"
    if tool_name == "scan_all_alerts":
        return "Scan all workers for compliance alerts"
    return f"Execute {tool_name}"


# ─── Public API ───────────────────────────────────────────────────────────────

def execute_tool(tool_name: str, args: dict) -> Any:
    handler = TOOL_HANDLERS.get(tool_name)
    if not handler:
        return {"error": f"Unknown tool: {tool_name}"}
    return handler(args)


def process_message(message: str, history: list, pending_tool_result: dict | None = None) -> dict:
    """
    Main entry point. Returns { reply, tool_calls, executed_results }.

    tool_calls entries:
      { id, name, args, needs_confirmation, preview? (if pending), result? (if executed) }
    """
    contents: list[types.Content] = []
    for msg in history[-10:]:
        role = "user" if msg.get("role") == "user" else "model"
        contents.append(types.Content(role=role, parts=[types.Part(text=msg.get("content", ""))]))

    if pending_tool_result:
        tool_summary = (
            f"[Tool '{pending_tool_result.get('tool_name')}' was confirmed and executed. "
            f"Result: {json.dumps(pending_tool_result.get('result', {}), default=str)}] "
            f"Please summarize the outcome for the user."
        )
        contents.append(types.Content(role="user", parts=[types.Part(text=tool_summary)]))
    else:
        contents.append(types.Content(role="user", parts=[types.Part(text=message)]))

    cfg = types.GenerateContentConfig(
        tools=[types.Tool(function_declarations=TOOL_SCHEMAS)],
        system_instruction=SYSTEM_PROMPT,
        temperature=0.2,
    )
    response = _client.models.generate_content(model=REASONING_MODEL, contents=contents, config=cfg)

    # Collect function calls from the response
    function_calls = []
    if response.candidates:
        for part in response.candidates[0].content.parts:
            if hasattr(part, "function_call") and part.function_call:
                fc = part.function_call
                function_calls.append({"name": fc.name, "args": dict(fc.args or {})})

    if not function_calls:
        return {
            "reply": response.text or "I couldn't process that. Please try rephrasing.",
            "tool_calls": [],
            "executed_results": [],
        }

    tool_calls_out = []
    executed_results = []

    for fc in function_calls:
        name, args = fc["name"], fc["args"]
        tc_id = f"{name}_{hash(str(args)) & 0xFFFFFF:06x}"

        if name in MUTATING_TOOLS:
            tool_calls_out.append({
                "id": tc_id,
                "name": name,
                "args": args,
                "needs_confirmation": True,
                "preview": _build_preview(name, args),
            })
        else:
            result = execute_tool(name, args)
            executed_results.append({"tool": name, "result": result})
            tool_calls_out.append({
                "id": tc_id,
                "name": name,
                "args": args,
                "needs_confirmation": False,
                "result": result,
            })

    # Pending confirmation — return preview without summarising
    if any(t["needs_confirmation"] for t in tool_calls_out):
        previews = "; ".join(t["preview"] for t in tool_calls_out if t["needs_confirmation"])
        return {
            "reply": f"I can do that — please confirm: {previews}",
            "tool_calls": tool_calls_out,
            "executed_results": [],
        }

    # All read-only — ask Gemini to narrate results
    results_text = json.dumps(executed_results, indent=2, default=str)
    summary_contents = contents + [
        types.Content(role="model", parts=[types.Part(text="[called tools, got results]")]),
        types.Content(
            role="user",
            parts=[types.Part(text=f"Tool results:\n{results_text}\n\nSummarize for the user in plain English.")],
        ),
    ]
    summary = _client.models.generate_content(
        model=REASONING_MODEL,
        contents=summary_contents,
        config=types.GenerateContentConfig(temperature=0.2),
    )
    return {
        "reply": summary.text or "Done.",
        "tool_calls": tool_calls_out,
        "executed_results": executed_results,
    }
