def _worker_name(d: dict, doc_id: str) -> str:
    return (
        d.get("full_name")
        or (d.get("passport") or {}).get("full_name")
        or d.get("master_name")
        or doc_id
    )


def _handle_list_workers(args: dict) -> dict:
    status_filter = args.get("status_filter")
    workers = []
    for doc in db.collection("workers").stream():
        d = doc.to_dict() or {}
        rs = d.get("review_status", "unknown")
        if status_filter and rs != status_filter:
            continue
        workers.append({
            "worker_id": doc.id,
            "name": _worker_name(d, doc.id),
            "nationality": d.get("nationality") or (d.get("passport") or {}).get("nationality", ""),
            "sector": d.get("sector") or (d.get("general_information") or {}).get("sector", ""),
            "review_status": rs,
            "workflow_status": d.get("workflow_status", ""),
        })
    return {"workers": workers, "total": len(workers)}


def _handle_find_worker_by_name(args: dict) -> dict:
    query = (args.get("name") or "").lower()
    matches = []
    for doc in db.collection("workers").stream():
        d = doc.to_dict() or {}
        full = (_worker_name(d, "")).lower()
        if query in full:
            matches.append({
                "worker_id": doc.id,
                "name": _worker_name(d, doc.id),
                "review_status": d.get("review_status", ""),
                "workflow_status": d.get("workflow_status", ""),
            })
    return {"matches": matches, "count": len(matches)}


def _handle_list_alerts(args: dict) -> dict:
    severity_filter = args.get("severity")
    now = datetime.now()
    alerts = []
    for doc in db.collection("workers").stream():
        d = doc.to_dict() or {}
        name = _worker_name(d, doc.id)

        for date_key, alert_type, label in [
            (
                d.get("permit_expiry_date") or (d.get("general_information") or {}).get("permit_expiry_date"),
                "permit",
                "Permit",
            ),
            (
                d.get("passport_expiry_date") or (d.get("passport") or {}).get("expiry_date"),
                "passport",
                "Passport",
            ),
        ]:
            if not date_key:
                continue
            try:
                exp = datetime.fromisoformat(str(date_key))
                days = (exp - now).days
                if days < 0:
                    sev = "critical"
                    msg = f"{label} expired {abs(days)} days ago"
                elif days <= 30:
                    sev = "high"
                    msg = f"{label} expires in {days} days"
                elif days <= 90:
                    sev = "medium"
                    msg = f"{label} expires in {days} days"
                else:
                    continue
                if not severity_filter or sev == severity_filter:
                    alerts.append({"worker_id": doc.id, "name": name, "type": f"{alert_type}_expiry", "severity": sev, "message": msg})
            except Exception:
                pass

        if d.get("fomema_status", "").lower() in ("pending", ""):
            sev = "high"
            if not severity_filter or sev == severity_filter:
                alerts.append({"worker_id": doc.id, "name": name, "type": "fomema_pending", "severity": sev, "message": "FOMEMA medical screening pending"})

    return {"alerts": alerts, "total": len(alerts)}


def _handle_get_quota_balance(args: dict) -> dict:
    company_id = args.get("company_id", "demo-company")
    doc = db.collection("companies").document(company_id).get()
    if not doc.exists:
        return {"error": f"Company '{company_id}' not found"}
    d = doc.to_dict() or {}
    return {
        "company_name": d.get("company_name", ""),
        "quota_balance": d.get("quota_balance", {}),
        "quota_approved": d.get("quota_approved"),
        "jtksm_60k_status": d.get("jtksm_60k_status", ""),
    }


def _handle_list_obligations(args: dict) -> dict:
    worker_id = args["worker_id"]
    docs = db.collection("worker_obligations").where("worker_id", "==", worker_id).stream()
    obligations = sorted(
        [{"id": doc.id, **({k: v for k, v in (doc.to_dict() or {}).items()})} for doc in docs],
        key=lambda x: x.get("date") or "",
    )
    return {"worker_id": worker_id, "obligations": obligations}


def _handle_list_contracts(args: dict) -> dict:
    status_filter = args.get("status")
    contracts = []
    for doc in db.collection("contracts").stream():
        d = doc.to_dict() or {}
        if status_filter and d.get("status") != status_filter:
            continue
        contracts.append({
            "contract_id": doc.id,
            "worker_name": d.get("worker_name"),
            "worker_id": d.get("worker_id"),
            "status": d.get("status"),
            "created_at": d.get("created_at"),
        })
    return {"contracts": contracts, "total": len(contracts)}


def _handle_list_pending_handoffs(args: dict) -> dict:
    handoffs = []
    for doc in db.collection("pending_handoffs").stream():
        d = doc.to_dict() or {}
        if d.get("status") == "awaiting_confirmation":
            handoffs.append({
                "id": doc.id,
                "worker_id": d.get("worker_id"),
                "action_type": d.get("action_type"),
                "triggered_by": d.get("triggered_by"),
            })
    return {"pending_handoffs": handoffs, "total": len(handoffs)}


