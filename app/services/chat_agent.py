def _handle_start_compliance_workflow(args: dict) -> dict:
    worker_id = args["worker_id"]
    doc = db.collection("workers").document(worker_id).get()
    if not doc.exists:
        return {"error": f"Worker '{worker_id}' not found"}
    worker_data = doc.to_dict() or {}
    try:
        from app.agents.graph import compliance_graph
        from app.agents.state import create_initial_worker_state
        state = create_initial_worker_state(worker_data)
        state["worker_id"] = worker_id
        state["trace"] = []
        state["current_node"] = None
        config = {"configurable": {"thread_id": f"worker_{worker_id}", "checkpoint_ns": "compliance"}}
        result = compliance_graph.invoke(state, config)
        db.collection("workflows").document(worker_id).set({
            "worker_id": worker_id,
            "status": "active",
            "current_state": result,
            "started_at": datetime.now(timezone.utc).isoformat(),
            "last_updated": datetime.now(timezone.utc).isoformat(),
        })
        return {
            "worker_id": worker_id,
            "status": result.get("compliance_status"),
            "hitl_required": result.get("hitl_required", False),
            "alerts": result.get("alerts", []),
        }
    except Exception as e:
        return {"error": str(e)}


